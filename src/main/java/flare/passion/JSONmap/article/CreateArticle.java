package flare.passion.JSONmap.article;

public class CreateArticle {
    int id;
    String title;
    String thumbnail;
    String content;
    String source;
    int type;
    String rumorExtObj;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getContent() {
        return content;
    }

    public int getType() {
        return type;
    }

    public String getRumorExtObj() {
        return rumorExtObj;
    }

    public String getSource() {
        return source;
    }
}
